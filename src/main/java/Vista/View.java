    private void limpiarCamposLineaProductos() {
        listaDeProductos.setSelectedItem(ABORT);
        cantidadCarrito.setText("");
    }
    
    private void FillJList() {
        DefaultListModel lista = new DefaultListModel();
        int index = 0;
        
        for (LineaProducto linea : listaLineaProductos) {
            int cantidad = linea.getCantidad();
            String nombre = linea.getProducto().getNombre();
            int precio = linea.getProducto().getPrecio();
            String data = nombre + " ---- " + "  " + cantidad + " ---- " + " Precio total = " + (cantidad * precio);
            
            lista.add(index, data);
            index++;
        }
        listaCarrito.setModel(lista);
    }
    
